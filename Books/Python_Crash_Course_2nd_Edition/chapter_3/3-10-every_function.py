categories = ['Pwn', 'Reverse', 'Web', 'Crypto', 'Misc']

# Use the sort() method
categories.sort()
print(categories)

# Add reverse=True argument to the sort() method
categories.sort(reverse=True)
print(categories)

# Use the sorted() function
print(sorted(categories))

# Use the reverse() method
categories.reverse()
print(categories)

# Use the len() function
print(len(categories))